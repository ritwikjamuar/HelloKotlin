fun main ( args : Array < String > ) {
	// This prints the given argument in the console without a new line.
	print ( "This is a print statement" )

	// This prints the given argument in the console with a new line.
	println ( "This is also a print statement" )

	// We can print any values by passing the desired value as argument to print () or println () methods.
	println ( 11 )
}